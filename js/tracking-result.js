import { db } from "./firebase-config.js";

import {
  collection,
  query,
  where,
  getDocs
} from "https://www.gstatic.com/firebasejs/12.14.0/firebase-firestore.js";

const result = document.getElementById("result");

const params = new URLSearchParams(window.location.search);

const trackingNumber = params.get("tracking");

loadRecord();

async function loadRecord() {

  try {

    if (!trackingNumber) {

      result.innerHTML = `
        <div class="tracking-card">

          <div class="card-header">

            <img
              src="images/logo1.png"
              class="report-logo"
              alt="SecureTrust Vault International">

            <h2>SecureTrust Vault International</h2>

            <p>Asset Verification System</p>

          </div>

          <h3 style="color:red;">
            No Tracking Number Provided
          </h3>

          <a href="index.html" class="hero-btn">
            Return Home
          </a>

        </div>
      `;

      return;
    }

    const q = query(
      collection(db, "vaultRecords"),
      where("trackingNumber", "==", trackingNumber)
    );

    const snapshot = await getDocs(q);

    if (snapshot.empty) {

      result.innerHTML = `
        <div class="tracking-card">

          <div class="card-header">

            <img
              src="images/logo1.png"
              class="report-logo"
              alt="SecureTrust Vault International">

            <h2>SecureTrust Vault International</h2>

            <p>Asset Verification System</p>

          </div>

          <h3 style="color:red;">
            Tracking Number Not Found
          </h3>

          <p>
            The tracking number you entered could not be found in our records.
          </p>

          <br>

          <a href="index.html" class="hero-btn">
            Verify Another Asset
          </a>

        </div>
      `;

      return;
    }

    const data = snapshot.docs[0].data();

    let statusClass = "blue";

    switch (data.status) {

      case "In Storage":
        statusClass = "green";
        break;

      case "Released":
        statusClass = "red";
        break;

      case "Awaiting Release":
        statusClass = "orange";
        break;

      case "Under Verification":
        statusClass = "blue";
        break;
    }

    result.innerHTML = `

      <div class="tracking-card">

        <div class="card-header">

          <img
            src="images/logo1.png"
            class="report-logo"
            alt="SecureTrust Vault International">

          <h2>
            SecureTrust Vault International
          </h2>

          <p>
            Official Asset Verification Report
          </p>

          <div class="verification-seal">
            ✓ VERIFIED RECORD
          </div>

        </div>

        <div class="status-badge ${statusClass}">
          ${data.status}
        </div>

        <div class="details-grid">

          <div>
            <strong>Tracking Number</strong>
            <p>${data.trackingNumber}</p>
          </div>

          <div>
            <strong>Client Name</strong>
            <p>${data.clientName}</p>
          </div>

          <div>
            <strong>Phone Number</strong>
            <p>${data.phoneNumber}</p>
          </div>

          <div>
            <strong>Home Address</strong>
            <p>${data.homeAddress}</p>
          </div>

          <div>
            <strong>Item Description</strong>
            <p>${data.itemDescription}</p>
          </div>

          <div>
            <strong>Category</strong>
            <p>${data.category}</p>
          </div>

          <div>
            <strong>Quantity</strong>
            <p>${data.quantity}</p>
          </div>

          <div>
            <strong>Weight</strong>
            <p>${data.weight} KG</p>
          </div>

          <div>
            <strong>Storage Location</strong>
            <p>${data.storageLocation}</p>
          </div>

          <div>
            <strong>Date Received</strong>
            <p>${data.dateReceived}</p>
          </div>

        </div>

        <div class="remarks-box">

          <h3>Official Remarks</h3>

          <p>
            ${data.remarks}
          </p>

        </div>

        <div class="report-footer">

          This report was generated by
          SecureTrust Vault International.

        </div>

        <div
          style="
            margin-top:25px;
            text-align:center;
          ">

          <a
            onclick="window.print()"
            class="hero-btn">

            Print Report

          </a>

          <a
            href="index.html"
            class="hero-btn"
            style="margin-left:10px;">

            Verify Another Asset

          </a>

        </div>

      </div>

    `;

  } catch (error) {

    console.error(error);

    result.innerHTML = `
      <div class="tracking-card">

        <h2>Error Loading Record</h2>

        <p>
          ${error.message}
        </p>

      </div>
    `;
  }
}